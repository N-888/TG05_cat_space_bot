from aiogram import F, Router  # Импортируем фильтры F и класс Router для обработки сообщений и кнопок.
from aiogram.filters import Command, CommandStart, StateFilter  # Импортируем фильтры для команд и проверки отсутствия состояния.
from aiogram.types import CallbackQuery, Message  # Импортируем типы входящих событий Telegram.

from app.keyboards.inline import get_main_menu_keyboard  # Импортируем клавиатуру главного меню.

router = Router()  # Создаём роутер для общих команд и подсказок.

START_TEXT = (  # Создаём красивый стартовый текст для первого сообщения пользователю.
    "<b>✨ CatSpace Bot</b>\n\n"  # Показываем стильный заголовок бота.
    "Я умею показывать котиков по породе, присылать случайных пушистиков и находить космическую картинку дня от NASA.\n\n"  # Объясняем главные возможности бота.
    "<b>Что можно сделать прямо сейчас:</b>\n"  # Открываем блок действий.
    "• нажать <b>🐱 Случайный котик</b>;\n"  # Подсказываем кнопку для случайного котика.
    "• нажать <b>🔎 Найти породу</b>;\n"  # Подсказываем кнопку для поиска породы.
    "• нажать <b>🚀 Космос сегодня</b>;\n"  # Подсказываем кнопку для APOD за сегодня.
    "• нажать <b>🌌 Случайный космос</b>."  # Подсказываем кнопку для случайного APOD.
)  # Завершаем стартовый текст.

HELP_TEXT = (  # Создаём подробный текст помощи по командам.
    "<b>🆘 Справка по командам</b>\n\n"  # Показываем заголовок раздела помощи.
    "<b>Основные команды:</b>\n"  # Открываем список основных команд.
    "/start — открыть красивое стартовое меню\n"  # Объясняем команду запуска.
    "/menu — снова показать главное меню\n"  # Объясняем команду повторного открытия меню.
    "/help — показать эту подсказку\n\n"  # Объясняем команду помощи.
    "<b>Кошки:</b>\n"  # Открываем блок кошачьих команд.
    "/cat_random — прислать случайную фотографию котика\n"  # Объясняем команду случайного котика.
    "/cat_breed — начать ввод названия породы пошагово\n"  # Объясняем команду пошагового поиска породы.
    "/cat_breed Bengal — сразу найти породу по названию\n\n"  # Показываем пример команды с аргументом.
    "<b>Космос:</b>\n"  # Открываем блок космических команд.
    "/space_today — картинка NASA APOD за сегодня\n"  # Объясняем команду APOD за сегодня.
    "/space_random — случайная APOD за последний год\n"  # Объясняем команду случайной APOD.
    "/space_date 2025-12-31 — APOD по конкретной дате"  # Показываем пример команды по дате.
)  # Завершаем текст помощи.


@router.message(CommandStart())  # Объявляем обработчик команды /start.
async def start_handler(message: Message) -> None:  # Создаём асинхронную функцию ответа на /start.
    await message.answer(START_TEXT, reply_markup=get_main_menu_keyboard())  # Отправляем приветствие и главное меню.


@router.message(Command("help"))  # Объявляем обработчик команды /help.
async def help_handler(message: Message) -> None:  # Создаём асинхронную функцию ответа на /help.
    await message.answer(HELP_TEXT, reply_markup=get_main_menu_keyboard())  # Показываем справку и главное меню.


@router.message(Command("menu"))  # Объявляем обработчик команды /menu.
async def menu_handler(message: Message) -> None:  # Создаём асинхронную функцию ответа на /menu.
    await message.answer("<b>📋 Главное меню</b>", reply_markup=get_main_menu_keyboard())  # Показываем компактное сообщение и главное меню.


@router.callback_query(F.data == "open_main_menu")  # Обрабатываем нажатие кнопки возврата в меню.
async def open_main_menu_handler(callback: CallbackQuery) -> None:  # Создаём асинхронную функцию для callback-кнопки меню.
    await callback.answer()  # Закрываем анимацию нажатия кнопки у пользователя.
    await callback.message.answer("<b>📋 Главное меню</b>", reply_markup=get_main_menu_keyboard())  # Отправляем главное меню отдельным сообщением.


@router.callback_query(F.data == "show_help")  # Обрабатываем нажатие кнопки помощи.
async def show_help_handler(callback: CallbackQuery) -> None:  # Создаём асинхронную функцию для показа справки по кнопке.
    await callback.answer()  # Закрываем индикатор нажатия кнопки.
    await callback.message.answer(HELP_TEXT, reply_markup=get_main_menu_keyboard())  # Показываем подробную справку и меню.


@router.message(StateFilter(None), F.text)  # Ловим обычный текст только тогда, когда пользователь не находится в пошаговом сценарии.
async def fallback_text_handler(message: Message) -> None:  # Создаём асинхронную функцию для текстовой подсказки.
    await message.answer(  # Отправляем дружелюбную подсказку вместо молчания бота.
        "<b>🙂 Я понял сообщение, но для надёжной работы лучше использовать кнопки или команды.</b>\n\n"  # Объясняем, что бот ждёт команды или нажатия кнопок.
        "Нажмите одну из кнопок ниже или отправьте /help.",  # Подсказываем пользователю следующий шаг.
        reply_markup=get_main_menu_keyboard(),  # Добавляем главное меню под ответом.
    )  # Завершаем отправку подсказки.